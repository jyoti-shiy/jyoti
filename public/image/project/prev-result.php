    <script>
        document.addEventListener('DOMContentLoaded', function () {
    const tabs = document.querySelectorAll('.nav-link');
    const tabContents = document.querySelectorAll('.tab-pane');
    const resultTitle = document.getElementById('result-title');

    const apiUrls = {
        home: 'https://shillongteerresults.app/public/api/shillong-results',
        profile: 'https://shillongteerresults.app/public/api/khanapara-results',
        contact: 'https://shillongteerresults.app/public/api/juwai-results',
        night: 'https://shillongteerresults.app/public/api/night-results',
        morning: 'https://shillongteerresults.app/public/api/bhutan-morning-results',
        teer: 'https://shillongteerresults.app/public/api/bhutan-teer-results'
    };

    tabs.forEach(tab => {
        tab.addEventListener('click', function () {
            const targetTab = this.getAttribute('data-bs-target').substring(7); // Removes the '#pills-' part

            tabs.forEach(btn => btn.classList.remove('active'));
            this.classList.add('active');

            tabContents.forEach(content => content.classList.remove('show', 'active'));
            document.getElementById(`pills-${targetTab}`).classList.add('show', 'active');

            // Update result title dynamically
            resultTitle.textContent = `${targetTab.charAt(0).toUpperCase() + targetTab.slice(1)} Previous Results`;

            fetchResults(targetTab, apiUrls[targetTab]);
        });
    });

    function fetchResults(tab, apiUrl, page = 1) {
        fetch(`${apiUrl}?page=${page}`)
            .then(response => {
                if (!response.ok) {
                    throw new Error('Network response was not ok');
                }
                return response.json();
            })
            .then(responseData => {
                const data = responseData.data;
                const tbody = document.getElementById(`results-${tab}`);

                if (page === 1) {
                    tbody.innerHTML = ''; // Clear previous data
                }

                data.forEach(result => {
                    const tr = document.createElement('tr');
                    const dateTd = document.createElement('td');
                    const frTd = document.createElement('td');
                    const srTd = document.createElement('td');

                    const date = new Date(result.created_at);
                    dateTd.textContent = `${date.getDate()} ${date.toLocaleString('default', { month: 'long' })} ${date.getFullYear()}`;
                    frTd.textContent = result.frvalue;
                    srTd.textContent = result.srvalue;

                    tr.appendChild(dateTd);
                    tr.appendChild(frTd);
                    tr.appendChild(srTd);
                    tbody.appendChild(tr);
                });

                if (responseData.current_page < responseData.last_page) {
                    fetchResults(tab, apiUrl, responseData.current_page + 1);
                }
            })
            .catch(error => {
                console.error('Error fetching data:', error);
            });
    }

    // Fetch initial data for the default active tab
    fetchResults('home', apiUrls.home);
});

